package com.sapient.week8.part7;

public class UserInput {
	
	private int n1, n2, result;
	
	public UserInput() {
		n1=0;
		n2=0;
		result=0;
	}

	public int getN1() {
		return n1;
	}

	public void setN1(int n1) {
		this.n1 = n1;
	}

	public int getN2() {
		return n2;
	}

	public void setN2(int n2) {
		this.n2 = n2;
	}

	public int getResult() {
		return result;
	}

	public void setResult(int result) {
		this.result = result;
	}

}
